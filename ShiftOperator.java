class ShiftOpt{
    public static void main(String[] s) {

        //left shift <<
        int val =  0b0010;
        int newVal = val << 1;//dec 4
        //right shift >>
        int value  = 0b0101;
        int newValue  = value >> 1;// dec 2

        //print
        System.out.println(newVal);
        System.out.println(newValue);
    }

}